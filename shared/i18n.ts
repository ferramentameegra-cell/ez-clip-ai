export function getTranslation(key: string, language: Language = 'pt-BR'): string {
  const keys = key.split('.');
  
  // Função auxiliar para buscar tradução
  const findTranslation = (lang: Language): string | undefined => {
    let value: any = translations[lang];
    
    for (const k of keys) {
      if (value && typeof value === 'object' && k in value) {
        value = value[k];
      } else {
        return undefined;
      }
    }
    
    return typeof value === 'string' ? value : undefined;
  };
  
  // Tentar encontrar a tradução no idioma solicitado
  let result = findTranslation(language);
  
  // Se não encontrou, tentar fallback para português
  if (!result && language !== 'pt-BR') {
    result = findTranslation('pt-BR');
  }
  
  // Se ainda não encontrou, retornar a chave
  return result || key;
}

